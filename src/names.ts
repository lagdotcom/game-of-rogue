function koNamegen(g: Game) {
    const family = oneOf(g.rng, koFamily);
    const initial = oneOf(g.rng, koInitial);

    while (true) {
        const finial = oneOf(g.rng, koFinial);
        if (initial !== finial) {
            return `${family} ${initial}-${finial.toLowerCase()}`;
        }
    }
}

export function randomName(g: Game) {
    return oneOf(g.rng, [jpNamegen, zhNamegen, koNamegen])(g);
}
